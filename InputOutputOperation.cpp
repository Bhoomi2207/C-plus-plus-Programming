#include<iostream>
using namespace std;
int main()
{
    /********************************Input - output variables****************************/
    int n1, n2;
    cout<<"Enter the value of number 1: ";  /* The symbol '<<' is is called Insertion operator. */
    cin>>n1; /* The symbol '>>' is called Extraction operator. */

    cout<<"\nEnter the value of number 2: ";  /* The symbol '<<' is is called Insertion operator. */
    cin>>n2; /* The symbol '>>' is called Extraction operator. */

    cout<<"The number 1 is: "<<n1<<" and the number 2: "<<n2;
    return 0;
}

/*
  The 'cout' is used for printing function. 
  The 'cin' is used to take input from userr.
  The symbol << is called the Insertion operator. 
  The ssymbole >> is called the Extraction operrator. */
